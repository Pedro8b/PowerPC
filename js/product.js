function updateThumbnails(thumbnails, title) {
    const thumbnailsContainer = document.querySelector('.image-thumbnails');
    thumbnailsContainer.innerHTML = '';
    
    thumbnails.forEach((thumb, index) => {
        const img = document.createElement('img');
        img.src = thumb;
        img.alt = `${title} - –≤–∏–¥ ${index + 1}`;
        img.className = 'thumbnail';
        if (index === 0) img.classList.add('active');
        
        img.addEventListener('click', function() {
            // –û–±–Ω–æ–≤–ª—è–µ–º –≥–ª–∞–≤–Ω–æ–µ –∏–∑–æ–±—Ä–∞–∂–µ–Ω–∏–µ
            document.getElementById('mainImage').src = this.src;
            document.getElementById('mainImage').alt = this.alt;
            
            // –£–±–∏—Ä–∞–µ–º –∞–∫—Ç–∏–≤–Ω—ã–π –∫–ª–∞—Å—Å —É –≤—Å–µ—Ö –º–∏–Ω–∏–∞—Ç—é—Ä
            document.querySelectorAll('.thumbnail').forEach(t => t.classList.remove('active'));
            // –î–æ–±–∞–≤–ª—è–µ–º –∞–∫—Ç–∏–≤–Ω—ã–π –∫–ª–∞—Å—Å —Ç–µ–∫—É—â–µ–π –º–∏–Ω–∏–∞—Ç—é—Ä–µ
            this.classList.add('active');
        });
        
        thumbnailsContainer.appendChild(img);
    });
}

